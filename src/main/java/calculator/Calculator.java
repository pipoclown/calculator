package calculator;

public class Calculator {
	//step 1
	int add(int i, int j){
		return i + j;
	}
	
	//step 2
	int subtract(int i, int j){
		return i - j;
	}
	
	//step 3
	int divide(int i, int j){
		return i / j;
	}
	
	//step 4
	int multiply(int i, int j){
		return i * j;
	}
	
	// step 5 
	int sum (int i, int j, int k){
		return i + j + k;
	}
	
	// step 6 
	int minsum (int i, int j, int k){
		return i - j - k;
	}
	
}